def dfs(maze, visit_fn):            #│  "  │  █│█│█ █│^ "│ │ │     │        "│ │
    max_x = len(maze[0])            #│ ╶─┬─┴─┐ │ ├───┼─╴ ╵ │ ├─────┘ ┌───┬─╴ │ │
    max_y = len(maze)               #│" .│^ ^│█│█│█ █│    .│l│       │  .│   │ │
    fringe = [(1, 1)]               #├─╴ │ ┌─┘ │ ╵ ╷ ╵ ┌───┘ │ ╶───┬─┘ ╷ └───┘ │
    seen = set()                    #│^  │.│. █│█ █│█  │t < >│.    │   │    w  │
    while len(fringe) > 0:          #│ ┌─┘ ╵ ╶─┴───┴─┬─┘ ┌───┴───┐ │ ┌─┴───┬─╴ │
        c = fringe.pop()            #│ │" "   █ █ █ █│t i│    o  │ │ │. ^ "│o  │
        x, y = c                    #│ │ ┌─────────┐ │ ╶─┤ ╶───┐ └─┘ │ ┌───┤ ╶─┤
        if c in seen:               #│.│ │. "   ^ ^│.│  l│l l  │     │^│  ^│^ ^│
            continue                #│ └─┤ ╶─┬───┐ └─┴─┐ └───┐ └───┬─┘ │ ╷ └─╴ │
        seen.add(c)                 #│  "│   │^  │^ .  │     │e H >│  ^│r│     │
        visit_fn(c, maze)           #├─╴ └─┐ ├─╴ └─┐ ╶─┴─┬─┐ ├───╴ │ ╷ │ └─────┤
        if is_end(c, maze):         #│  .  │.│█ █ █│█ . "│ │e│█ █ █│█│ │  ^ " l│
            return                  #│ ┌───┘ ╵ ┌─╴ └─┐ ╷ │ │ ╵ ┌───┘ │ └─┬───╴ │
        ns = neighbours(c, maze)    #│ │  ^ " █│█ █ █│ │.│.│  █│█ █ █│   │d    │
        for n in ns:                #│ │ ┌───┬─┴───┬─┘ │ ╵ ├───┴───╴ ├─╴ │ ┌───┤
            fringe.append(n)        #│"│ │  ^│█ █ █│█ .│. "│  █ █ █ █│   │^│  .│
