def is_wall(x, y, maze):            #│   │  o│█ █│█ █│█ █ █│█│█ █ █ █  │.│   │ │
    return maze[y][x] != ' '        #│ ╷ └───┼───┴─┐ │ ╶───┤ └─┬─────┬─┘ │ ┌─┘ │
                                    #│ │     │█ █ █│█│"   ^│  █│█ █ █│  "│ │  .│
def is_end(cell, maze):             #│ ├───┐ │ ┌─╴ │ ├───╴ └─┐ ╵ ┌─╴ │ ╶─┤ └───┤
    x, y = cell                     #│ │" .│ │█│█ █│█│  "   "│█ █│█ █│  ^│  ^  │
    max_x = len(maze[0])            #│ │ ╷ ╵ ╵ │ ╶─┴─┘ ┌─╴ ┌─┴───┤ ┌─┴─┐ ├───┐ │
    max_y = len(maze)               #│ │^│  " █│█ █ █ █│█ █│█ █ █│█│█  │.│" "│"│
    return ((x+2 == max_x) and      #│ └─┴─┬───┴─────┬─┘ ┌─┤ ╷ ╷ │ │ ╷ ╵ │ ╷ │ │
            (y+2 == max_y))         #│^    │" █ █ █ █│█ █│█│█│█│█│█│█│"  │ │ │ │
