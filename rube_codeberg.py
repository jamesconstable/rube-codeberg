def write_cell(cell, maze):         #│  .│^ "│█ █│█ █│   │. . █│█ █│█│  ^│^│ │ │
    x, y = cell                     #│ ┌─┘ ╷ └─┐ ├─┬─┘ ╷ └─────┤ ╷ ╵ │ ╷ ╵ │ └─┤
    if maze[y][x] not in ' █':      #│ │" .│  █│█│█│█ █│█ █ █ █│█│█ █│ │   │  ^│
        message.append(maze[y][x])  #│ ├───┘ ┌─┘ │ │ ┌─┴─────┐ ╵ ├───┘ ├───┴─┐ │
                                    #│^│     │█ █│█│█│█ █ █ █│█ █│█ █ ^│"    │"│
def neighbours(cell, maze):         #│ ╵ ┌─╴ │ ┌─┘ │ └───╴ ╷ └───┤ ┌───┘ ┌─╴ │ │
    x, y = cell                     #│  .│   │.│"  │  █ █ █│█    │!│. .  │^  │^│
